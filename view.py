# Lista é uma coleção ordenada e mutável. Permite membros ducplicados.
lista = ["automóvel", True, 2 , 2.5]
print(lista)
print(type(lista))
print("-"*30)


#Tupla é uma coleção ordenada e imutavel. Permite membros duplicados.
tupla = ("avião", True, 2, 2.5)
print(tupla)
print(type(tupla))
print("-"*30)

# O dicionário é uma coleção ordenada e mutavél. nenhum membro duplicado.
